package org.padre;

public class PegiDTO {
    private Long pkpegi;
    private String rangopegi;
    private String observaciones;

    public Long getPkpegi() {
        return this.pkpegi;
    }

    public void setPkpegi(Long pkpegi) {
        this.pkpegi = pkpegi;
    }

    public String getRangopegi() {
        return this.rangopegi;
    }

    public void setRangopegi(String rangopegi) {
        this.rangopegi = rangopegi;
    }

    public String getObservaciones() {
        return this.observaciones;
    }

    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }
}
